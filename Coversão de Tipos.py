# Primeiro exemplo, mudando valor inteiro para um valor flutuante (flaot)

preco = 10
print(preco)
# imprime 10

preco = float(preco)
print(preco)
# imprime 10.0

preco = 10 / 2
print(preco)
# imprime 5.0






# agora mudando de float para inteiro
preco = 10.30
print(preco)
 # imprime 10.3

preco = int(preco)
print(preco)
# imprime 10

preco = 10
print(preco)
# imprime 10

print (preco / 2)
# imprime 5.0

print (preco // 2) # aqui neste caso onde vemos duas barras, por ser duas, ele acaba preservando o resultado para o número inteiro!
# imprime 5





