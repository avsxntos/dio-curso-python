#Adição, Subtração e Multiplicação
#Operadores Aritméticos


 # Adição
print(1 + 2)
# 3

# Subtração
print(10 - 2)
# 8

# Multiplicação
print(4 * 3)
# 12

# Divisão  e Divisão Inteira
# Divisão 
print(12 / 3)
# 4.0

# Divisão Inteira 
print(12 // 2)
# 6


# Módulo e exponenciação 
# Módulo 
print(10 % 3)
# 1

# Exponenciação
print(2 ** 3)
# 8




# Hierarquia de Cálculos

print(10 - 5 * 2)
# 0

print((10 - 5) * 2) # o parenteses tendo a ser feito antes, por conta de sua importancia
# 10

print(10 ** 2 * 2)
# 200

print(10 ** (2 * 2))
# 10000

print(10 / 2 * 4)
# 20.0

